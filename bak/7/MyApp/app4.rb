# encoding: cp866

def print_greeting name    #опередяем процедуру с "названием print greeting", в которой присутствует переменная name

puts "Hello, #{name}"   #процедура выводит на экран сообщение Hello, %username%. при этом значение переменной name будет взято при вызове

end  #окончание процедуры

print_greeting "Roman" #вызов процедуры. Справа от названия процедуры указано значение переменной name
